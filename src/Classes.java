
public class Classes {

	// Definição da classe Produto
	public class Produto {
		String nome;       // Nome do produto
		double preco;      // Preço do produto
		int quantidade;    // Quantidade em estoque
	}

	// Definição da classe Pessoa
	public class Pessoa {
		String nome;       // Nome da pessoa
		int idade;         // Idade da pessoa
		boolean estudante;  // Se a pessoa é estudante (true ou false)
	}

	// Definição da classe Livro
	public class Livro {
		String titulo;     // Título do livro
		String autor;      // Autor do livro
		int anoPublicacao; // Ano de publicação do livro
	}

	// Definição da classe Carro
	public class Carro {
		String modelo;     // Modelo do carro
		String cor;        // Cor do carro
		int ano;           // Ano de fabricação do carro
	}

	// Definição da classe Estudante
	public class Estudante {
		String nome;       // Nome do estudante
		int matricula;     // Número de matrícula
		double nota;       // Nota do estudante
	}

	/* Esses exemplos mostram classes simples que contêm apenas atributos, representando diferentes entidades, como Produto, Pessoa, Livro, Carro, e Estudante. Cada atributo é do tipo String, int, ou boolean, que são tipos básicos em Java.
	*/
}